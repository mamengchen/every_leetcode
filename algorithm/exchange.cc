//输入两个整数a和b,交换二者的值，然后输出．
//样例输入：
//824   16
//样例输出：
//16    824
//按照题目所说，先把输入存入变量a和b，然后交换.如何交换两个变量呢？
int a, b, c;
cin >> a >> b;
c = a;
a = b;
b = a;

//第二种
int a, b;
cin >> a >> b;
a = a + b;
b = a - b;
a = a - b;
//但是这个种方式存在加法溢出

//第三种
int a, b;
cin >> a >> b;
a = a ^ b;
a = a ^ b;
b = a ^ b;
//这种方式效率不是最高的

//第四种方式效率最高
int a, b;
cin >> a >> b;
cout << b << a;
